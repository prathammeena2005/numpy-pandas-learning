import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# 1. Addition
print(a + b)

# 2. Subtraction
print(a - b)

# 3. Multiplication
print(a * b)

# 4. Division
print(a / b)

# 5. Floor Division
print(a // b)

# 6. Modulus
print(a % b)

# 7. Power
print(a ** 2)